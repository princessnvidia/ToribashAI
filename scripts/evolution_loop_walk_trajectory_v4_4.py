def initial_state():
    return {
        "generation": 0,
        "best_summary": {
            "score": -999999.0,
            "valid_steps": 0,
            "pec_stability": 0.0,
            "max_pec_diff": 999.0,
            "forward_progress": 0.0,
            "fallen": True,
        },
        "accepted": 0,
        "rejected": 0,
    }


def load_state():
    if STATE_PATH.exists():
        return load_json(STATE_PATH)
    return initial_state()


def save_state(state):
    save_json(STATE_PATH, state)


def init_champion():
    if CHAMPION_PATH.exists():
        return normalize_agent(load_json(CHAMPION_PATH))

    if SEED_PATH.exists():
        seed = normalize_agent(load_json(SEED_PATH))
        seed["name"] = "walk_trajectory_v4_4_seed_from_ypska"
        save_json(CHAMPION_PATH, seed)
        return seed

    raise FileNotFoundError(f"Missing seed: {SEED_PATH}")


# =========================
# MAIN LOOP
# =========================

def evaluate_candidate(agent):
    export_agent_to_lua(agent)
    result = run_episode()
    return summarize_result(result)


def print_summary(prefix, summary):
    print(
        f"{prefix} "
        f"score={summary['score']:.3f} "
        f"steps={summary['valid_steps']} "
        f"pec={summary['pec_stability']:.3f} "
        f"maxpec={summary['max_pec_diff']:.3f} "
        f"progress={summary['forward_progress']:.3f} "
        f"fallen={summary['fallen']} "
        f"reason={summary['reason']}"
    )


def main():
    ensure_dirs()

    champion = init_champion()
    state = load_state()

    print("=== ToribashAI Walk Trajectory Evolution v4.4 ===")
    print(f"Runner Lua: {RUNNER_LUA}")
    print(f"Champion:   {CHAMPION_PATH}")
    print(f"Result:     {RESULT_JSON}")
    print("")
    print("Nouvelle règle v4.4 :")
    print("- chute avant 2 vrais pas = rejet")
    print("- chute après 2 vrais pas + pecs corrects = candidat comptabilisé")
    print("")

    # Evaluate champion once if needed
    if safe_float(state.get("best_summary", {}).get("score", -999999.0)) <= -999998:
        print("Évaluation du champion initial...")
        champ_summary = evaluate_candidate(champion)
        state["best_summary"] = champ_summary
        save_state(state)
        print_summary("CHAMPION_INIT", champ_summary)
    else:
        champ_summary = state["best_summary"]
        print_summary("CHAMPION_LOADED", champ_summary)

    generation = safe_int(state.get("generation", 0))

    while generation < GENERATIONS:
        generation += 1
        state["generation"] = generation

        print("")
        print(f"========== GENERATION {generation} ==========")

        accepted_this_gen = 0

        for i in range(POPULATION_SIZE):
            candidate = mutate(champion, generation=generation)

            candidate_path = POP_DIR / f"gen{generation:06d}_cand{i:03d}.json"
            save_json(candidate_path, candidate)

            summary = evaluate_candidate(candidate)
            print_summary(f"CAND {i:03d}", summary)

            reject = hard_reject_reason(summary, champ_summary)

            if reject:
                state["rejected"] = safe_int(state.get("rejected", 0)) + 1
                print(f"  -> REJECTED: {reject}")
                save_state(state)
                continue

            if better_than_champion(summary, champ_summary):
                print("  -> ACCEPTED: new champion")

                champion = candidate
                champ_summary = summary

                save_json(CHAMPION_PATH, champion)
                save_json(BEST_DIR / f"champion_gen{generation:06d}_cand{i:03d}.json", champion)
                save_json(BEST_DIR / f"champion_gen{generation:06d}_cand{i:03d}_summary.json", champ_summary)

                state["best_summary"] = champ_summary
                state["accepted"] = safe_int(state.get("accepted", 0)) + 1
                accepted_this_gen += 1

                save_state(state)
            else:
                state["rejected"] = safe_int(state.get("rejected", 0)) + 1
                print("  -> KEPT OLD CHAMPION")
                save_state(state)

        print("")
        print_summary("BEST", champ_summary)
        print(f"Accepted this gen: {accepted_this_gen}")
        print(f"Total accepted: {state.get('accepted', 0)} | rejected: {state.get('rejected', 0)}")

        time.sleep(0.1)


if __name__ == "__main__":
    main()
