def mutation_rate_for_frame(frame):
    # 0 -> 126 = launch YpSkA protégé : aucune mutation
    if frame < FROZEN_LAUNCH_UNTIL:
        return 0.0

    # 126 -> 170 = zone marche principale : mutation forte
    if frame <= STRONG_MUTATION_UNTIL:
        return EARLY_MUTATION_RATE

    # 170+ = ajustements plus doux
    return BASE_MUTATION_RATE


def mutate(agent, generation=0):
    parent = normalize_agent(agent)
    child = json.loads(json.dumps(parent))

    child["name"] = f"walk_trajectory_v4_4_gen{generation}_{now_ms()}"
    child["branch"] = "walk_trajectory_v4_4"
    child["parent"] = parent.get("name", "unknown")

    mutations = 0

    for cmd in child["commands"]:
        frame = safe_int(cmd.get("frame", 0))
        rate = mutation_rate_for_frame(frame)

        new_pairs = []

        for joint, state in cmd["pairs"]:
            # Ne jamais supprimer de joints dans le launch gelé
            if frame < FROZEN_LAUNCH_UNTIL:
                new_pairs.append([joint, state])
                continue

            if random.random() < PAIR_DROP_RATE:
                mutations += 1
                continue

            if random.random() < rate:
                state = random.choice(JOINT_STATES)
                mutations += 1

            new_pairs.append([joint, state])

        if random.random() < PAIR_ADD_RATE:
            existing = {p[0] for p in new_pairs}
            possible = [j for j in JOINTS if j not in existing]
            if possible:
                new_pairs.append([random.choice(possible), random.choice(JOINT_STATES)])
                mutations += 1

        cmd["pairs"] = new_pairs

    # Sometimes add a new command near the useful early walking zone
    if random.random() < 0.20:
        frame = random.randint(FROZEN_LAUNCH_UNTIL, min(EARLY_FRAME_LIMIT, LOOP_LENGTH - 1))
        pair_count = random.randint(1, 4)
        joints = random.sample(JOINTS, pair_count)
        pairs = [[j, random.choice(JOINT_STATES)] for j in joints]
        child["commands"].append({"frame": frame, "pairs": pairs})
        mutations += pair_count

    child["mutation_count"] = mutations
    return normalize_agent(child)


