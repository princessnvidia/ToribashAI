def xdotool(*args, check=False):
    try:
        return subprocess.run(["xdotool", *args], check=check, capture_output=True, text=True)
    except Exception as e:
        print(f"[xdotool error] {e}")
        return None


def focus_toribash():
    subprocess.run(
        ["bash", "-lc", "xdotool search --onlyvisible --class Toribash windowactivate %@ 2>/dev/null || true"],
        capture_output=True,
        text=True,
    )


def toribash_chat_command(command):
    focus_toribash()
    time.sleep(0.05)

    xdotool("key", "t")
    time.sleep(0.05)

    subprocess.run(["bash", "-lc", f"printf %s {json.dumps(command)} | xclip -selection clipboard"], check=False)
    time.sleep(0.05)

    xdotool("key", "ctrl+a")
    time.sleep(0.03)
    xdotool("key", "BackSpace")
    time.sleep(0.03)
    xdotool("key", "ctrl+v")
    time.sleep(0.03)
    xdotool("key", "Return")


def reset_toribash():
    toribash_chat_command(RESET_COMMAND)
    time.sleep(WAIT_AFTER_RESET)


def run_episode():
    if RESULT_JSON.exists():
        try:
            RESULT_JSON.unlink()
        except Exception:
            pass

    reset_toribash()

    start = time.time()

    while time.time() - start < RESULT_TIMEOUT:
        if RESULT_JSON.exists():
            try:
                data = load_json(RESULT_JSON)
                if isinstance(data, dict) and "score" in data:
                    return data
            except Exception:
                pass
        time.sleep(0.05)

    return {
        "score": -999999.0,
        "reason": "timeout",
        "fallen": True,
        "valid_steps": 0,
        "pec_stability": 0.0,
        "max_pec_diff": 999.0,
    }


