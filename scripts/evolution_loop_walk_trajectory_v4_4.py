def normalize_agent(agent):
    if isinstance(agent, list):
        agent = {
            "name": "legacy_list_agent",
            "branch": "walk_trajectory_v4_4",
            "loop_length": LOOP_LENGTH,
            "commands": agent,
        }

    agent.setdefault("name", "walk_trajectory_v4_4_agent")
    agent.setdefault("branch", "walk_trajectory_v4_4")
    agent.setdefault("loop_length", LOOP_LENGTH)
    agent.setdefault("commands", [])

    clean_commands = []

    for cmd in agent["commands"]:
        frame = safe_int(cmd.get("frame", 0))
        pairs = cmd.get("pairs", [])

        clean_pairs = []
        seen = set()

        for pair in pairs:
            if isinstance(pair, dict):
                joint = safe_int(pair.get("joint", -1), -1)
                state = safe_int(pair.get("state", -1), -1)
            else:
                if len(pair) < 2:
                    continue
                joint = safe_int(pair[0], -1)
                state = safe_int(pair[1], -1)

            if joint not in JOINTS:
                continue
            if state not in JOINT_STATES:
                continue
            if joint in seen:
                continue

            seen.add(joint)
            clean_pairs.append([joint, state])

        if clean_pairs:
            clean_commands.append({
                "frame": frame % LOOP_LENGTH,
                "pairs": clean_pairs,
            })

    clean_commands.sort(key=lambda c: c["frame"])
    agent["commands"] = clean_commands
    return agent


def agent_to_lua(agent):
    agent = normalize_agent(agent)

    lines = []
    lines.append("-- Auto-generated by evolution_loop_walk_trajectory_v4_4.py")
    lines.append("TORIBASHAI_AGENT = {}")
    lines.append(f"TORIBASHAI_AGENT.name = {json.dumps(agent.get('name', 'walk_trajectory_v4_4'))}")
    lines.append(f"TORIBASHAI_AGENT.branch = {json.dumps(agent.get('branch', 'walk_trajectory_v4_4'))}")
    lines.append(f"TORIBASHAI_AGENT.loop_length = {int(agent.get('loop_length', LOOP_LENGTH))}")
    lines.append("TORIBASHAI_AGENT.commands = {")

    for cmd in agent["commands"]:
        frame = safe_int(cmd["frame"])
        pair_chunks = []
        for joint, state in cmd["pairs"]:
            pair_chunks.append(f"{{{int(joint)}, {int(state)}}}")
        pairs_lua = ", ".join(pair_chunks)
        lines.append(f"  {{ frame = {frame}, pairs = {{ {pairs_lua} }} }},")

    lines.append("}")
    lines.append("")
    lines.append("-- lowercase alias too, just in case")
    lines.append("toribashai_agent = TORIBASHAI_AGENT")
    lines.append("")

    return "\n".join(lines)

def export_agent_to_lua(agent):
    AGENT_LUA.write_text(agent_to_lua(agent), encoding="utf-8")


