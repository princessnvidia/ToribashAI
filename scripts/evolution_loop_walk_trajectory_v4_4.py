def summarize_result(result):
    return {
        "score": safe_float(result.get("score", -999999.0)),
        "reason": result.get("reason", "unknown"),
        "fallen": bool(result.get("fallen", False)),
        "valid_steps": safe_int(result.get("valid_steps", 0)),
        "left_steps": safe_int(result.get("left_steps", 0)),
        "right_steps": safe_int(result.get("right_steps", 0)),
        "same_foot_steps": safe_int(result.get("same_foot_steps", 0)),
        "hop_steps": safe_int(result.get("hop_steps", 0)),
        "knee_ground": safe_int(result.get("knee_ground", result.get("knee_touch", 0))),
        "forward_fall": bool(result.get("forward_fall", False)),
        "side_tilt": bool(result.get("side_tilt", False)),
        "pec_stability": safe_float(result.get("pec_stability", 0.0)),
        "max_pec_diff": safe_float(result.get("max_pec_diff", 999.0)),
        "forward_progress": safe_float(result.get("forward_progress", 0.0)),
        "frames": safe_int(result.get("frames", 0)),
    }


def is_real_walk(summary):
    return (
        summary["valid_steps"] >= MIN_REAL_WALK_STEPS
        and summary["pec_stability"] >= MIN_REAL_WALK_PEC_STABILITY
        and summary["same_foot_steps"] == 0
        and summary["hop_steps"] == 0
    )


def hard_reject_reason(summary, champion_summary):
    real_walk = is_real_walk(summary)

    if summary["same_foot_steps"] > 0:
        return "same_foot_steps"

    if summary["hop_steps"] > 0:
        return "hop_steps"

    if summary["knee_ground"] > 0:
        return "knee_ground"

    if summary["forward_fall"]:
        return "forward_fall"

    if summary["side_tilt"] and not real_walk:
        return "side_tilt_before_walk"

    # Nouvelle logique v4.4 :
    # chute avant vraie marche = rejet
    # chute après vraie marche = autorisée
    if summary["fallen"] and not real_walk:
        return "fallen_before_real_walk"

    # Pecs ultra stricts avant vraie marche
    if not real_walk and summary["max_pec_diff"] > MAX_PEC_DIFF_HARD_PRE_WALK:
        return "pec_diff_too_high_before_walk"

    # Pecs encore limités après vraie marche, mais plus souple
    if real_walk and summary["max_pec_diff"] > MAX_PEC_DIFF_ACCEPT_AFTER_WALK:
        return "pec_diff_too_high_after_walk"

    # Curriculum freeze : ne pas perdre les pas déjà acquis
    champion_steps = safe_int(champion_summary.get("valid_steps", 0))
    champion_pec = safe_float(champion_summary.get("pec_stability", 0.0))

    if champion_steps >= MIN_REAL_WALK_STEPS:
        if summary["valid_steps"] < champion_steps:
            return "regressed_valid_steps"

    if champion_pec >= MIN_REAL_WALK_PEC_STABILITY:
        if summary["pec_stability"] + 1e-9 < champion_pec:
            return "regressed_pec_stability"

    return None


def better_than_champion(summary, champion_summary):
    score = summary["score"]
    best_score = safe_float(champion_summary.get("score", -999999.0))

    steps = summary["valid_steps"]
    best_steps = safe_int(champion_summary.get("valid_steps", 0))

    pec = summary["pec_stability"]
    best_pec = safe_float(champion_summary.get("pec_stability", 0.0))

    progress = summary["forward_progress"]
    best_progress = safe_float(champion_summary.get("forward_progress", 0.0))

    # Priorité 1 : plus de vrais pas
    if steps > best_steps:
        return True

    # Priorité 2 : même nombre de pas, pecs plus stables
    if steps == best_steps and pec > best_pec + 0.01:
        return True

    # Priorité 3 : même marche, plus de progression
    if steps == best_steps and abs(pec - best_pec) <= 0.03 and progress > best_progress + 0.10:
        return True

    # Priorité 4 : score global
    if score > best_score + MIN_SCORE_IMPROVEMENT:
        return True

    return False


